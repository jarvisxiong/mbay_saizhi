package org.sz.mbay.trafficred.bean;

/**
 * 三大运营商流量包数量
 * 
 * @author jerry
 */
public class ClassifyPackageNums {
	
	private int mobiles;
	
	private int unicoms;
	
	private int telecoms;
	
	public int getMobiles() {
		return mobiles;
	}
	
	public void setMobiles(int mobiles) {
		this.mobiles = mobiles;
	}
	
	public int getUnicoms() {
		return unicoms;
	}
	
	public void setUnicoms(int unicoms) {
		this.unicoms = unicoms;
	}
	
	public int getTelecoms() {
		return telecoms;
	}
	
	public void setTelecoms(int telecoms) {
		this.telecoms = telecoms;
	}
}
